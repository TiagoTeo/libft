#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	char		*d;
	const char	*s;
	char		*finald;
	const char	*finals;

	d = dest;
	s = src;
	if (!dest && !src)
		return (NULL);
	if (d < s)
	{
		while (n--)
			*d++ = *s++;
	}
	else
	{
		finald = d + (n - 1);
		finals = (char *)s + (n - 1);
		while (n--)
			*finald-- = *finals--;
	}
	return (dest);
}
